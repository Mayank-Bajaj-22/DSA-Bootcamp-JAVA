import java.util.Scanner;

public class Basics {
    public static void main(String[] args) {

        // if else syntax

        int a = 10;
        if (a == 10) {
            System.out.println("Hello Mayank");
        }
        else {
            System.out.println("None");
        }

        // while loop syntax

        int count = 1;
        while (count != 5) {
            System.out.println(count);
            count++;
        }

        // for loops

        for (int b = 1; b != 5; b++) {
            System.out.println(b);
        }

    }
}
